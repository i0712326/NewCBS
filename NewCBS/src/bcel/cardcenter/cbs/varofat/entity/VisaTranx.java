package bcel.cardcenter.cbs.varofat.entity;

import java.io.Serializable;
import java.sql.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;
@Entity
@Table(name="TRANXLOG")
public class VisaTranx implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	@Id
	@Column(name="TRANXDATE")
	private Date date;
	@Column(name="TERMINALID")
	private String terminalId;
	@Column(name="TRANXCODE")
	private String tranxCode;
	@Column(name="CARDNUMBER")
	private String cardNumber;
	@Column(name="AMOUNT")
	private double amount;
	@Column(name="CURRCODE")
	private String currCode;
	@Column(name="TRACENO")
	private String traceNo;
	@Column(name="REFNO")
	private String refNo;
	@Column(name="RESPONSECODE")
	private String responseCode;
	@Column(name="APPROVALCODE")
	private String approvalCode;
	@Column(name="TRANXDATE")
	private String tranxDate;
	@Column(name="TRANXTIME")
	private String tranxTime;
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public String getTerminalId() {
		return terminalId;
	}
	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}
	public String getTranxCode() {
		return tranxCode;
	}
	public void setTranxCode(String tranxCode) {
		this.tranxCode = tranxCode;
	}
	public String getCardNumber() {
		return cardNumber;
	}
	public void setCardNumber(String cardNumber) {
		this.cardNumber = cardNumber;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public String getCurrCode() {
		return currCode;
	}
	public void setCurrCode(String currCode) {
		this.currCode = currCode;
	}
	public String getTraceNo() {
		return traceNo;
	}
	public void setTraceNo(String traceNo) {
		this.traceNo = traceNo;
	}
	public String getRefNo() {
		return refNo;
	}
	public void setRefNo(String refNo) {
		this.refNo = refNo;
	}
	public String getResponseCode() {
		return responseCode;
	}
	public void setResponseCode(String responseCode) {
		this.responseCode = responseCode;
	}
	public String getApprovalCode() {
		return approvalCode;
	}
	public void setApprovalCode(String approvalCode) {
		this.approvalCode = approvalCode;
	}
	public String getTranxDate() {
		return tranxDate;
	}
	public void setTranxDate(String tranxDate) {
		this.tranxDate = tranxDate;
	}
	public String getTranxTime() {
		return tranxTime;
	}
	public void setTranxTime(String tranxTime) {
		this.tranxTime = tranxTime;
	}
}
